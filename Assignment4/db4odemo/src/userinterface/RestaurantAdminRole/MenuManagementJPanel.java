    private void populateTable() {
         DefaultTableModel model = (DefaultTableModel) tblRestaurant.getModel();
        
        model.setRowCount(0);
        
        for (String menu : r.getMenu()){
            Object[] row = new Object[1];
            row[0] = menu;
            model.addRow(row);
        }
    }
