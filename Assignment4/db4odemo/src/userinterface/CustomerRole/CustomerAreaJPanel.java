    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int row = tblRestaurant.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        selectedRestaurant = ((Restaurant)tblRestaurant.getValueAt(row, 0));
        DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
        
        model.setRowCount(0);
        if(selectedRestaurant.getMenu().size() == 0){
            JOptionPane.showMessageDialog(null, "Restaurant has no Item on menu", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for (String menu : selectedRestaurant.getMenu()){
            String[] row1 = new String[1];
            row1[0] = menu;
            model.addRow(row1);
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        // TODO add your handling code here:\
        int row = tblMenu.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Order order = system.getWorkQueue().createOrder();
        order.setDish(((String)tblMenu.getValueAt(row, 0)));
        order.setCustomer((Customer) userAccount.getWorkAreaObj());
        order.setStatus("Order Placed");
        order.setRestaurant(selectedRestaurant);
        populateCustomerTable();
    }//GEN-LAST:event_btnOrderActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        int row = tblcust.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Order order = ((Order)tblcust.getValueAt(row, 0));
        CommentManage c = new CommentManage();
        c.setComment(txtComment.getText());
        c.setDish(order.getDish());
        c.setCustomer(order.getCustomer());
        order.getRestaurant().getComment().add(c);
        JOptionPane.showMessageDialog(null, "Review comment submitted!", "Success", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnSubmitActionPerformed

