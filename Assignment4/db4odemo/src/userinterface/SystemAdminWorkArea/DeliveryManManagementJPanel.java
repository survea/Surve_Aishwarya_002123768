    private void populateTable() {
         DefaultTableModel model = (DefaultTableModel) tblDeliveryMan.getModel();
        
        model.setRowCount(0);
        
        for (DeliveryMan deliveryMan : ecosystem.getDeliveryManDirectory().getDeliveryManList()){
            Object[] row = new Object[1];
            row[0] = deliveryMan;
            model.addRow(row);
        }
    }
