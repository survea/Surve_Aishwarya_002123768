    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().isEmpty() || txtPrice.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter in all the fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else {
            try{
                int price = Integer.parseInt(txtPrice.getText());
            } catch(Exception w){
                JOptionPane.showMessageDialog(this, "Please enter price in numerical format", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Product product = supplier.getProductCatalog().addProduct();
            product.setName(txtName.getText());
            product.setLogoImage(logoImage);
            product.setPrice(Integer.parseInt(txtPrice.getText()));
            JOptionPane.showMessageDialog(this, "Product successfully added", "Information", JOptionPane.INFORMATION_MESSAGE);
            backAction();
        }
        
}//GEN-LAST:event_btnAddActionPerformed
    private void backAction() {
        workArea.remove(this);
        Component[] componentArray = workArea.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageProductCatalogJPanel manageProductCatalogJPanel = (ManageProductCatalogJPanel) component;
        manageProductCatalogJPanel.refreshTable();
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.previous(workArea);
    }
    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        backAction();
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnAttachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachActionPerformed
        // TODO add your handling code here:
        int returnVal = fileChooser.showOpenDialog(this);
        if(returnVal == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            URL url;
            try{
                url = file.toURI().toURL();
                logoImage = new ImageIcon(url);
                logoImage = new ImageIcon(logoImage.getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH));
                imgLogo.setIcon(logoImage);
            }catch(MalformedURLException e){
                Logger.getLogger(this.getName()).log(Level.SEVERE, null,e);
            }
        }
    }//GEN-LAST:event_btnAttachActionPerformed

