    private void btnAddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSupplierActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().isEmpty() || txtDesc.getText().isEmpty()){
          JOptionPane.showMessageDialog(this, "Please enter all the fields", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Supplier supplier = supplierDirectory.addSupplier();
            supplier.setSupplyName(txtName.getText());
            supplier.setSupplyDesc(txtDesc.getText());
            supplier.setLogoImage(logoImage);
            
            List<String> feature = new ArrayList<String>();
            DefaultTableModel model = (DefaultTableModel) tblFeature.getModel();
            for(int s =0; s < model.getRowCount(); s++){
                feature.add((String) model.getValueAt(s, 0));
            }
            supplier.setCommonFeatures(feature);
            JOptionPane.showMessageDialog(this, "Supplier successfully added", "Warning", JOptionPane.INFORMATION_MESSAGE);
            backAction();
        }
    }//GEN-LAST:event_btnAddSupplierActionPerformed

