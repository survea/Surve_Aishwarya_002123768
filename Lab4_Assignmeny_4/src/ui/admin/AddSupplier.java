    private void btnRemoveFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveFActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblFeature.getModel();
         int row = tblFeature.getSelectedRow();
         if(row < 0){
            JOptionPane.showMessageDialog(this,"Please select a row from the table first", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else{
             model.removeRow(row);
         }
    }//GEN-LAST:event_btnRemoveFActionPerformed

      
      private void backAction() {
        workArea.remove(this);
        Component[] componentArray = workArea.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageSuppliers manageSuppliersJPanel = (ManageSuppliers) component;
        manageSuppliersJPanel.refreshTable();
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.previous(workArea);
    }


